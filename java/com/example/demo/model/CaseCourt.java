package com.example.demo.model;


public class CaseCourt {
    private int courtID;
    private int caseID;
    private int catID;

    // Getter and Setter for courtID
    public int getCourtID() {
        return courtID;
    }

    public void setCourtID(int courtID) {
        this.courtID = courtID;
    }

    // Getter and Setter for caseID
    public int getCaseID() {
        return caseID;
    }

    public void setCaseID(int caseID) {
        this.caseID = caseID;
    }

    // Getter and Setter for catID
    public int getCatID() {
        return catID;
    }

    public void setCatID(int catID) {
        this.catID = catID;
    }
}
