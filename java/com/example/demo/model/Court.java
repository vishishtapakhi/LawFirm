package com.example.demo.model;

public class Court {

    private int courtID;        // Corresponds to CourtID
    private String courtName;   // Corresponds to CourtName
    private String courtPincode; // Corresponds to CourtPincode
    private String courtState;  // Corresponds to CourtState
    private String courtCity;   // Corresponds to CourtCity
    private String jFName;      // Corresponds to jFName
    private String jMName;      // Corresponds to jMName
    private String jLName;      // Corresponds to jLName

    // Getters and Setters
    public int getCourtID() {
        return courtID;
    }

    public void setCourtID(int courtID) {
        this.courtID = courtID;
    }

    public String getCourtName() {
        return courtName;
    }

    public void setCourtName(String courtName) {
        this.courtName = courtName;
    }

    public String getCourtPincode() {
        return courtPincode;
    }

    public void setCourtPincode(String courtPincode) {
        this.courtPincode = courtPincode;
    }

    public String getCourtState() {
        return courtState;
    }

    public void setCourtState(String courtState) {
        this.courtState = courtState;
    }

    public String getCourtCity() {
        return courtCity;
    }

    public void setCourtCity(String courtCity) {
        this.courtCity = courtCity;
    }

    public String getjFName() {
        return jFName;
    }

    public void setjFName(String jFName) {
        this.jFName = jFName;
    }

    public String getjMName() {
        return jMName;
    }

    public void setjMName(String jMName) {
        this.jMName = jMName;
    }

    public String getjLName() {
        return jLName;
    }

    public void setjLName(String jLName) {
        this.jLName = jLName;
    }
}
